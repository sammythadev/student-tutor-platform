/** Student's preferred learning modality — part of the style vector (Algorithm.md §2.1). */
export enum LearningStyle {
  /** Learns best through diagrams, charts, and visual aids. */
  VISUAL = 'visual',

  /** Learns best through listening and verbal explanation. */
  AUDITORY = 'auditory',

  /** Learns best through hands-on activities and movement. */
  KINESTHETIC = 'kinesthetic',
}
